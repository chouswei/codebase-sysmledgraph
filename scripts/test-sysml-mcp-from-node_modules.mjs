#!/usr/bin/env node
/**
 * Test SysML MCP using the server from node_modules (sysml-v2-lsp).
 * Run from repo root: npm run build && node scripts/test-sysml-mcp-from-node_modules.mjs
 *
 * Optional: SKIP_SPAWN=1 — only verify node_modules path and client API (no server spawn).
 */
import { createSysmlMcpClient, getMcpServerPath } from '../dist/src/parser/sysml-mcp-client.js';
import { resolve } from 'path';
import { existsSync } from 'fs';

const nodeModulesServer = resolve(process.cwd(), 'node_modules/sysml-v2-lsp/dist/server/mcpServer.js');
if (!existsSync(nodeModulesServer)) {
  console.error('Missing node_modules/sysml-v2-lsp. Run: npm install');
  process.exit(1);
}

const serverPath = getMcpServerPath();
console.log('Server path:', serverPath);
console.log('Using node_modules:', nodeModulesServer);

if (process.env.SKIP_SPAWN === '1') {
  console.log('\nSKIP_SPAWN=1: node_modules server path OK, client API loaded.');
  process.exit(0);
}

console.log('');

const code = `package NodeModulesTest {
  requirement R1 {
    doc /* test */ "Requirement from node_modules test.";
  }
}`;
const uri = 'file:///node_modules-test.sysml';

const client = await createSysmlMcpClient({
  serverPath: nodeModulesServer,
  initTimeout: 30000,
});
try {
  const parseResult = await client.parse(code, uri);
  console.log('parse:', JSON.stringify(parseResult, null, 2));

  const validateResult = await client.validate(code, uri);
  console.log('validate:', JSON.stringify(validateResult, null, 2));

  const symbolsResult = await client.getSymbols(code, uri);
  console.log('getSymbols count:', symbolsResult?.count ?? 0);

  console.log('\nOK — test using node_modules/sysml-v2-lsp passed.');
} catch (err) {
  console.error('Error:', err.message);
  process.exit(1);
} finally {
  client.close();
}
